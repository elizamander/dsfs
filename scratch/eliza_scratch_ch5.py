def median(nums: list) -> int: 
    sort_nums = sorted(nums)
    n = len(sort_nums)
    mid = n // 2
    if n % 2 == 1:
        return sort_nums[mid]
    else:
        return (sort_nums[(mid-1)] + sort_nums[mid]) / 2

